    private const SSH_TIMEOUT = 30;
    private const READ_TIMEOUT = 30;
    private const PROMPT_REGEX = '/[#>\$\]]\s*$/';
    private const STABLE_WAIT = 2;

    public function __construct(
        #[Autowire('%kernel.project_dir%')]
        private readonly string $projectDir,
    ) {}

    private function serializeRule(CollectionRule $r): array
    {
        return [
            'id' => $r->getId(),
            'name' => $r->getName(),
            'description' => $r->getDescription(),
            'enabled' => $r->isEnabled(),
            'source' => $r->getSource(),
            'command' => $r->getCommand(),
            'tag' => $r->getTag(),
            'folderId' => $r->getFolder()?->getId(),
            'extracts' => array_values(array_map(
                $this->serializeExtract(...),
                $r->getExtracts()->toArray()
            )),
            'createdAt' => $r->getCreatedAt()->format('c'),
        ];
    }

    private function serializeFolder(CollectionRuleFolder $f, EntityManagerInterface $em): array
    {
        $rules = $em->getRepository(CollectionRule::class)->findBy(
            ['folder' => $f],
            ['name' => 'ASC']
        );

        $children = $em->getRepository(CollectionRuleFolder::class)->findBy(
            ['parent' => $f],
            ['name' => 'ASC']
        );

        return [
            'id' => $f->getId(),
            'name' => $f->getName(),
            'type' => $f->getType(),
            'parentId' => $f->getParent()?->getId(),
            'manufacturerId' => $f->getManufacturer()?->getId(),
            'modelId' => $f->getModel()?->getId(),
            'autoGenerated' => $f->isAutoGenerated(),
            'children' => array_map(fn($c) => $this->serializeFolder($c, $em), $children),
            'rules' => array_map($this->serializeRule(...), $rules),
        ];
    }

    #[Route('/tree', methods: ['GET'])]
    public function tree(Request $request, EntityManagerInterface $em): JsonResponse
    {
        $contextId = $request->query->getInt('context');
        if (!$contextId) {
            return $this->json(['folders' => [], 'rootRules' => []]);
        }

        $rootFolders = $em->getRepository(CollectionRuleFolder::class)->findBy(
            ['context' => $contextId, 'parent' => null],
            ['name' => 'ASC']
        );

        $rootRules = $em->getRepository(CollectionRule::class)->findBy(
            ['context' => $contextId, 'folder' => null],
            ['name' => 'ASC']
        );

        return $this->json([
            'folders' => array_map(fn($f) => $this->serializeFolder($f, $em), $rootFolders),
            'rootRules' => array_map($this->serializeRule(...), $rootRules),
        ]);
    }

    #[Route('/by-model/{id}', methods: ['GET'])]
    public function byModel(DeviceModel $model, EntityManagerInterface $em): JsonResponse
    {
        $modelFolder = $em->getRepository(CollectionRuleFolder::class)->findOneBy([
            'model' => $model,
            'type' => CollectionRuleFolder::TYPE_MODEL,
        ]);

        $manFolder = $em->getRepository(CollectionRuleFolder::class)->findOneBy([
            'manufacturer' => $model->getManufacturer(),
            'model' => null,
            'type' => CollectionRuleFolder::TYPE_MANUFACTURER,
        ]);

        $result = [];

        $collectRecursive = function (CollectionRuleFolder $folder, bool $inherited, bool $skipModelFolders = false) use ($em, &$result, &$collectRecursive): void {
            $rules = $em->getRepository(CollectionRule::class)->findBy(['folder' => $folder], ['name' => 'ASC']);
            foreach ($rules as $r) {
                $entry = $this->serializeRule($r);
                $entry['inherited'] = $inherited;
                $entry['association'] = 'auto';
                $result[] = $entry;
            }
            $children = $em->getRepository(CollectionRuleFolder::class)->findBy(['parent' => $folder], ['name' => 'ASC']);
            foreach ($children as $child) {
                if ($skipModelFolders && $child->getType() === CollectionRuleFolder::TYPE_MODEL) continue;
                $collectRecursive($child, $inherited, $skipModelFolders);
            }
        };

        if ($manFolder) {
            $collectRecursive($manFolder, true, true);
        }
        if ($modelFolder) {
            $collectRecursive($modelFolder, false);
        }

        // Manually associated rules
        $seenIds = array_map(fn($e) => $e['id'], $result);
        foreach ($model->getManualRules() as $r) {
            if (in_array($r->getId(), $seenIds, true)) continue;
            $entry = $this->serializeRule($r);
            $entry['inherited'] = false;
            $entry['association'] = 'manual';
            $result[] = $entry;
        }

        return $this->json($result);
    }

    #[Route('/by-model/{id}/associate', methods: ['POST'])]
    public function associate(DeviceModel $model, Request $request, EntityManagerInterface $em): JsonResponse
    {
        $data = json_decode($request->getContent(), true);
        $ruleIds = $data['ruleIds'] ?? [];

        foreach ($ruleIds as $ruleId) {
            $rule = $em->getRepository(CollectionRule::class)->find($ruleId);
            if ($rule) {
                $model->addManualRule($rule);
            }
        }

        $em->flush();

        return $this->json(['ok' => true]);
    }

    #[Route('/by-model/{id}/dissociate', methods: ['POST'])]
    public function dissociate(DeviceModel $model, Request $request, EntityManagerInterface $em): JsonResponse
    {
        $data = json_decode($request->getContent(), true);
        $ruleId = $data['ruleId'] ?? null;

        if ($ruleId) {
            $rule = $em->getRepository(CollectionRule::class)->find($ruleId);
            if ($rule) {
                $model->removeManualRule($rule);
            }
        }

        $em->flush();

        return $this->json(['ok' => true]);
    }

    #[Route('/available-for-model/{id}', methods: ['GET'])]
    public function availableForModel(DeviceModel $model, EntityManagerInterface $em): JsonResponse
    {
        $context = $model->getContext();

        $excludeIds = array_map(fn($r) => $r->getId(), $model->getManualRules()->toArray());

        $modelFolder = $em->getRepository(CollectionRuleFolder::class)->findOneBy([
            'model' => $model,
            'type' => CollectionRuleFolder::TYPE_MODEL,
        ]);
        $manFolder = $em->getRepository(CollectionRuleFolder::class)->findOneBy([
            'manufacturer' => $model->getManufacturer(),
            'model' => null,
            'type' => CollectionRuleFolder::TYPE_MANUFACTURER,
        ]);

        if ($modelFolder) {
            foreach ($em->getRepository(CollectionRule::class)->findBy(['folder' => $modelFolder]) as $r) {
                $excludeIds[] = $r->getId();
            }
        }
        if ($manFolder) {
            foreach ($em->getRepository(CollectionRule::class)->findBy(['folder' => $manFolder]) as $r) {
                $excludeIds[] = $r->getId();
            }
        }

        // Build tree of custom folders only, with available rules
        $rootFolders = $em->getRepository(CollectionRuleFolder::class)->findBy(
            ['context' => $context, 'parent' => null, 'type' => CollectionRuleFolder::TYPE_CUSTOM],
            ['name' => 'ASC']
        );

        $folders = [];
        foreach ($rootFolders as $f) {
            $node = $this->buildAvailableRuleTree($f, $em, $excludeIds);
            if ($node) $folders[] = $node;
        }

        return $this->json(['folders' => $folders]);
    }

    private function buildAvailableRuleTree(CollectionRuleFolder $folder, EntityManagerInterface $em, array $excludeIds): ?array
    {
        $rules = [];
        foreach ($em->getRepository(CollectionRule::class)->findBy(['folder' => $folder], ['name' => 'ASC']) as $r) {
            if (!in_array($r->getId(), $excludeIds, true)) {
                $rules[] = [
                    'id' => $r->getId(),
                    'name' => $r->getName(),
                    'description' => $r->getDescription(),
                    'enabled' => $r->isEnabled(),
                ];
            }
        }

        $children = [];
        foreach ($em->getRepository(CollectionRuleFolder::class)->findBy(['parent' => $folder, 'type' => CollectionRuleFolder::TYPE_CUSTOM], ['name' => 'ASC']) as $child) {
            $node = $this->buildAvailableRuleTree($child, $em, $excludeIds);
            if ($node) $children[] = $node;
        }

        if (empty($rules) && empty($children)) return null;

        return [
            'id' => $folder->getId(),
            'name' => $folder->getName(),
            'type' => $folder->getType(),
            'children' => $children,
            'rules' => $rules,
        ];
    }

    #[Route('', methods: ['POST'])]
    public function create(Request $request, EntityManagerInterface $em): JsonResponse
    {
        $data = json_decode($request->getContent(), true);
        $contextId = $request->query->getInt('context');
        $context = $contextId ? $em->getRepository(Context::class)->find($contextId) : null;
        if (!$context) {
            return $this->json(['error' => 'Context is required'], Response::HTTP_BAD_REQUEST);
        }

        $name = $data['name'] ?? '';
        if (empty($name)) {
            return $this->json(['error' => 'Name is required'], Response::HTTP_BAD_REQUEST);
        }

        $folder = null;
        $folderId = $data['folderId'] ?? null;
        if ($folderId) {
            $folder = $em->getRepository(CollectionRuleFolder::class)->find($folderId);
        }

        $rule = new CollectionRule();
        $rule->setName($name);
        $rule->setDescription($data['description'] ?? null);
        $rule->setEnabled($data['enabled'] ?? true);
        $rule->setSource($data['source'] ?? CollectionRule::SOURCE_LOCAL);
        $rule->setCommand($data['command'] ?? null);
        $rule->setTag($data['tag'] ?? null);
        $rule->setFolder($folder);
        $rule->setContext($context);

        $em->persist($rule);
        $em->flush();

        return $this->json($this->serializeRule($rule), Response::HTTP_CREATED);
    }

