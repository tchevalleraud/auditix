    #[Route('/{id}/extracts', methods: ['POST'])]
    public function createExtract(CollectionRule $rule, Request $request, EntityManagerInterface $em): JsonResponse
    {
        $data = json_decode($request->getContent(), true);
        $name = $data['name'] ?? '';
        $regex = $data['regex'] ?? '';

        if (empty($name) || empty($regex)) {
            return $this->json(['error' => 'Name and regex are required'], Response::HTTP_BAD_REQUEST);
        }

        // Set position to end
        $maxPos = 0;
        foreach ($rule->getExtracts() as $e) {
            if ($e->getPosition() > $maxPos) $maxPos = $e->getPosition();
        }

        $extract = new CollectionRuleExtract();
        $extract->setName($name);
        $extract->setRegex($regex);
        $extract->setMultiline($data['multiline'] ?? false);
        $extract->setKeyMode($data['keyMode'] ?? CollectionRuleExtract::KEY_MODE_MANUAL);
        $extract->setKeyManual($data['keyManual'] ?? null);

        $extract->setKeyGroup(isset($data['keyGroup']) ? (int)$data['keyGroup'] : null);
        $extract->setKeyLabel($data['keyLabel'] ?? null);
        $extract->setValueGroup(isset($data['valueGroup']) ? (int)$data['valueGroup'] : null);
        $extract->setValueMap($data['valueMap'] ?? null);

        if (!empty($data['keyExtractId'])) {
            $keyExt = $em->getRepository(CollectionRuleExtract::class)->find($data['keyExtractId']);
            $extract->setKeyExtract($keyExt);
        }
        if (!empty($data['categoryId'])) {
            $cat = $em->getRepository(InventoryCategory::class)->find($data['categoryId']);
            $extract->setCategory($cat);
        }
        $extract->setNodeField($data['nodeField'] ?? null);
        $extract->setNodeFieldGroup(isset($data['nodeFieldGroup']) ? (int)$data['nodeFieldGroup'] : null);

        $extract->setPosition($maxPos + 1);
        $rule->addExtract($extract);

        $em->flush();

        return $this->json($this->serializeExtract($extract), Response::HTTP_CREATED);
    }

    #[Route('/{ruleId}/extracts/{extractId}', methods: ['PUT'])]
    public function updateExtract(int $ruleId, int $extractId, Request $request, EntityManagerInterface $em): JsonResponse
    {
        $extract = $em->getRepository(CollectionRuleExtract::class)->find($extractId);
        if (!$extract || $extract->getRule()->getId() !== $ruleId) {
            return $this->json(['error' => 'Extract not found'], Response::HTTP_NOT_FOUND);
        }

        $data = json_decode($request->getContent(), true);
        if (isset($data['name'])) {
            $extract->setName($data['name']);
        }
        if (isset($data['regex'])) {
            $extract->setRegex($data['regex']);
        }
        if (isset($data['multiline'])) {
            $extract->setMultiline($data['multiline']);
        }
        if (isset($data['keyMode'])) {
            $extract->setKeyMode($data['keyMode']);
        }
        if (array_key_exists('keyManual', $data)) {
            $extract->setKeyManual($data['keyManual'] ?: null);
        }
        if (array_key_exists('keyGroup', $data)) {
            $extract->setKeyGroup($data['keyGroup'] !== null ? (int)$data['keyGroup'] : null);
        }
        if (array_key_exists('keyLabel', $data)) {
            $extract->setKeyLabel($data['keyLabel'] ?: null);
        }
        if (array_key_exists('valueGroup', $data)) {
            $extract->setValueGroup($data['valueGroup'] !== null ? (int)$data['valueGroup'] : null);
        }
        if (array_key_exists('valueMap', $data)) {
            $extract->setValueMap($data['valueMap']);
        }
        if (array_key_exists('keyExtractId', $data)) {
            $keyExt = $data['keyExtractId'] ? $em->getRepository(CollectionRuleExtract::class)->find($data['keyExtractId']) : null;
            $extract->setKeyExtract($keyExt);
        }
        if (array_key_exists('categoryId', $data)) {
            $cat = $data['categoryId'] ? $em->getRepository(InventoryCategory::class)->find($data['categoryId']) : null;
            $extract->setCategory($cat);
        }
        if (array_key_exists('nodeField', $data)) {
            $extract->setNodeField($data['nodeField'] ?: null);
        }
        if (array_key_exists('nodeFieldGroup', $data)) {
            $extract->setNodeFieldGroup($data['nodeFieldGroup'] !== null ? (int)$data['nodeFieldGroup'] : null);
        }

        $em->flush();

        return $this->json($this->serializeExtract($extract));
    }

    #[Route('/{id}/extracts/reorder', methods: ['POST'])]
    public function reorderExtracts(CollectionRule $rule, Request $request, EntityManagerInterface $em): JsonResponse
    {
        $data = json_decode($request->getContent(), true);
        $ids = $data['ids'] ?? [];

        $extractsById = [];
        foreach ($rule->getExtracts() as $e) {
            $extractsById[$e->getId()] = $e;
        }

        foreach ($ids as $pos => $id) {
            if (isset($extractsById[$id])) {
                $extractsById[$id]->setPosition($pos);
            }
        }

        $em->flush();

        return $this->json(array_values(array_map(
            $this->serializeExtract(...),
            $rule->getExtracts()->toArray()
        )));
    }

    #[Route('/{ruleId}/extracts/{extractId}', methods: ['DELETE'])]
    public function deleteExtract(int $ruleId, int $extractId, EntityManagerInterface $em): JsonResponse
    {
        $extract = $em->getRepository(CollectionRuleExtract::class)->find($extractId);
        if (!$extract || $extract->getRule()->getId() !== $ruleId) {
            return $this->json(['error' => 'Extract not found'], Response::HTTP_NOT_FOUND);
        }

        $em->remove($extract);
        $em->flush();

        return $this->json(null, Response::HTTP_NO_CONTENT);
    }

    #[Route('/{id}', methods: ['DELETE'])]
    public function delete(CollectionRule $rule, EntityManagerInterface $em): JsonResponse
    {
        $em->remove($rule);
        $em->flush();

        return $this->json(null, Response::HTTP_NO_CONTENT);
    }
