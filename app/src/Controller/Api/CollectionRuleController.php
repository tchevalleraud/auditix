    #[Route('/{id}', methods: ['PUT'])]
    public function update(CollectionRule $rule, Request $request, EntityManagerInterface $em): JsonResponse
    {
        $data = json_decode($request->getContent(), true);

        if (isset($data['name'])) {
            $rule->setName($data['name']);
        }
        if (array_key_exists('description', $data)) {
            $rule->setDescription($data['description']);
        }
        if (isset($data['enabled'])) {
            $rule->setEnabled($data['enabled']);
        }
        if (isset($data['source'])) {
            $rule->setSource($data['source']);
        }
        if (array_key_exists('command', $data)) {
            $rule->setCommand($data['command'] ?: null);
        }
        if (array_key_exists('tag', $data)) {
            $source = $data['source'] ?? $rule->getSource();
            $rule->setTag($source === CollectionRule::SOURCE_LOCAL ? ($data['tag'] ?: null) : null);
        }
        if (array_key_exists('folderId', $data)) {
            $folder = $data['folderId'] ? $em->getRepository(CollectionRuleFolder::class)->find($data['folderId']) : null;
            $rule->setFolder($folder);
        }

        $em->flush();

        return $this->json($this->serializeRule($rule));
    }

    #[Route('/{id}/test', methods: ['POST'])]
    public function test(CollectionRule $rule, Request $request, EntityManagerInterface $em): JsonResponse
    {
        $data = json_decode($request->getContent(), true);
        $nodeId = $data['nodeId'] ?? null;

        if (!$nodeId) {
            return $this->json(['error' => 'nodeId is required'], Response::HTTP_BAD_REQUEST);
        }

        $node = $em->getRepository(Node::class)->find($nodeId);
        if (!$node) {
            return $this->json(['error' => 'Node not found'], Response::HTTP_NOT_FOUND);
        }

        $command = $rule->getCommand();
        if (!$command) {
            return $this->json(['error' => 'No command configured on this rule'], Response::HTTP_BAD_REQUEST);
        }

        if ($rule->getSource() === CollectionRule::SOURCE_LOCAL) {
            return $this->testLocal($rule, $node, $em);
        }

        return $this->testSSH($rule, $node, $em);
    }

    private function testLocal(CollectionRule $rule, Node $node, EntityManagerInterface $em): JsonResponse
    {
        $command = $rule->getCommand();
        $tag = $rule->getTag();
        $commandSlug = $this->slugify($command);

        // Find the latest collection for this node, optionally filtered by tag
        $conn = $em->getConnection();
        $sql = 'SELECT id FROM collection WHERE node_id = :node AND status = :status';
        $params = ['node' => $node->getId(), 'status' => Collection::STATUS_COMPLETED];

        if ($tag) {
            $sql .= ' AND tags::text LIKE :tag';
            $params['tag'] = '%"' . $tag . '"%';
        }

        $sql .= ' ORDER BY completed_at DESC LIMIT 1';
        $row = $conn->fetchAssociative($sql, $params);
        $collection = $row ? $em->getRepository(Collection::class)->find($row['id']) : null;
        if (!$collection) {
            return $this->json([
                'success' => false,
                'error' => $tag
                    ? 'No completed collection found for this node with tag "' . $tag . '"'
                    : 'No completed collection found for this node',
            ]);
        }

        $baseDir = $this->projectDir . '/var/' . $collection->getStoragePath();

        // Search for a file matching the command slug in any command subdirectory
        $foundContent = null;
        if (is_dir($baseDir)) {
            $dirs = scandir($baseDir);
            foreach ($dirs as $dir) {
                if ($dir === '.' || $dir === '..') continue;
                $filePath = $baseDir . '/' . $dir . '/' . $commandSlug . '.txt';
                if (file_exists($filePath)) {
                    $foundContent = file_get_contents($filePath);
                    break;
                }
            }
        }

        if ($foundContent === null) {
            return $this->json([
                'success' => false,
                'error' => 'No collected file found for command "' . $command . '" in collection #' . $collection->getId(),
            ]);
        }

        return $this->json([
            'success' => true,
            'output' => $foundContent,
            'collectionId' => $collection->getId(),
            'source' => 'local',
        ]);
    }

    private function testSSH(CollectionRule $rule, Node $node, EntityManagerInterface $em): JsonResponse
    {
        $profile = $node->getProfile();
        $cliCred = $profile?->getCliCredential();

        if (!$cliCred || !$cliCred->getUsername()) {
            return $this->json([
                'success' => false,
                'error' => 'No CLI credentials configured on this node\'s profile',
            ]);
        }

        $ip = $node->getIpAddress();
        $port = $cliCred->getPort() ?: 22;
        $model = $node->getModel();
        $command = $rule->getCommand();

        try {
            $ssh = new SSH2($ip, $port, self::SSH_TIMEOUT);
            $ssh->enablePTY();

            if (!$ssh->login($cliCred->getUsername(), $cliCred->getPassword() ?? '')) {
                return $this->json([
                    'success' => false,
                    'error' => 'SSH authentication failed for ' . $cliCred->getUsername() . '@' . $ip . ':' . $port,
                ]);
            }

            $ssh->setTimeout(self::READ_TIMEOUT);

            // Read initial prompt/banner
            $this->readFullResponse($ssh);

            // Execute connection script
            $connectionScript = $model?->getConnectionScript();
            if ($connectionScript) {
                $scriptLines = array_filter(array_map('trim', explode("\n", $connectionScript)), fn($l) => $l !== '');
                foreach ($scriptLines as $line) {
                    $ssh->write($line . "\n");
                    $this->readFullResponse($ssh);
                }
            }

            // Execute the command
            $ssh->write($command . "\n");
            $response = $this->readFullResponse($ssh);

            // Clean up response: remove echoed command and trailing prompt
            $responseLines = explode("\n", $response);
            if (!empty($responseLines) && str_contains($responseLines[0], trim($command))) {
                array_shift($responseLines);
            }
            if (!empty($responseLines) && preg_match(self::PROMPT_REGEX, end($responseLines))) {
                array_pop($responseLines);
            }

            $ssh->disconnect();

            return $this->json([
                'success' => true,
                'output' => implode("\n", $responseLines),
                'source' => 'ssh',
            ]);

        } catch (\Throwable $e) {
            return $this->json([
                'success' => false,
                'error' => 'SSH error: ' . $e->getMessage(),
            ]);
        }
    }

    private function readFullResponse(SSH2 $ssh): string
    {
        $output = $ssh->read(self::PROMPT_REGEX, SSH2::READ_REGEX);

        $ssh->setTimeout(self::STABLE_WAIT);

        while (true) {
            $extra = @$ssh->read(self::PROMPT_REGEX, SSH2::READ_REGEX);
            if ($extra === false || $extra === '') {
                break;
            }
            $output .= $extra;
        }

        $ssh->setTimeout(self::READ_TIMEOUT);

        return $output;
    }

    private function slugify(string $text): string
    {
        $text = preg_replace('/[^a-z0-9]+/i', '-', $text);
        return strtolower(trim($text, '-'));
    }

    private function serializeExtract(CollectionRuleExtract $e): array
    {
        return [
            'id' => $e->getId(),
            'name' => $e->getName(),
            'regex' => $e->getRegex(),
            'multiline' => $e->isMultiline(),
            'keyMode' => $e->getKeyMode(),
            'keyManual' => $e->getKeyManual(),
            'keyExtractId' => $e->getKeyExtract()?->getId(),
            'keyGroup' => $e->getKeyGroup(),
            'keyLabel' => $e->getKeyLabel(),
            'valueGroup' => $e->getValueGroup(),
            'valueMap' => $e->getValueMap(),
            'categoryId' => $e->getCategory()?->getId(),
            'categoryName' => $e->getCategory()?->getName(),
            'categoryKeyLabel' => $e->getCategory()?->getKeyLabel(),
            'nodeField' => $e->getNodeField(),
            'nodeFieldGroup' => $e->getNodeFieldGroup(),
            'position' => $e->getPosition(),
        ];
    }

