    #[Route('/api/collection-rule-folders/{id}/export', methods: ['GET'], requirements: ['id' => '\d+'])]
    public function exportRuleFolder(CollectionRuleFolder $folder, EntityManagerInterface $em): Response
    {
        if ($folder->isAutoGenerated()) {
            return $this->json(['error' => 'Cannot export auto-generated folders'], Response::HTTP_FORBIDDEN);
        }

        $ruleRefIndex = 0;
        $extractRefIndex = 0;
