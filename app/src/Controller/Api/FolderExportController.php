        ];

        // Resolve keyExtractRef
        $this->resolveExtractRefsInFolder($data['folder'], $extractRefMap);

        $safeName = preg_replace('/[^a-zA-Z0-9_\-.]/', '_', $folder->getName());
        $json = json_encode($data, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES);

        return new Response($json, Response::HTTP_OK, [
            'Content-Type' => 'application/json',
            'Content-Disposition' => sprintf('attachment; filename="rules-%s.json"', $safeName),
