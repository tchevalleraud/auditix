    #[Route('/api/collection-folders/{id}/export', methods: ['GET'], requirements: ['id' => '\d+'])]
    public function exportCommandFolder(CollectionFolder $folder, EntityManagerInterface $em): Response
    {
        if ($folder->isAutoGenerated()) {
            return $this->json(['error' => 'Cannot export auto-generated folders'], Response::HTTP_FORBIDDEN);
        }

        $cmdRefIndex = 0;
        $data = [
            '_format' => 'auditix-command-folder-export',
            '_version' => 1,
            '_exportedAt' => (new \DateTimeImmutable())->format('c'),
            'folder' => $this->serializeCmdFolderTree($folder, $em, $cmdRefIndex),
        ];

        $safeName = preg_replace('/[^a-zA-Z0-9_\-.]/', '_', $folder->getName());
        $json = json_encode($data, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES);

        return new Response($json, Response::HTTP_OK, [
            'Content-Type' => 'application/json',
            'Content-Disposition' => sprintf('attachment; filename="commands-%s.json"', $safeName),
        ]);
    }

    #[Route('/api/collection-folders/preview-import', methods: ['POST'], priority: 10)]
    public function previewImportCommandFolder(Request $request, EntityManagerInterface $em): JsonResponse
    {
        $data = $this->parseImportFile($request, 'auditix-command-folder-export');
        if ($data instanceof JsonResponse) return $data;

        $folder = $data['folder'];
        $commandCount = $this->countCmdFolderCommands($folder);
        $subfolderCount = $this->countSubfolders($folder);

        return $this->json([
            'folderName' => $folder['name'],
            'commandCount' => $commandCount,
            'subfolderCount' => $subfolderCount,
        ]);
    }

    #[Route('/api/collection-folders/import', methods: ['POST'], priority: 10)]
    public function importCommandFolder(Request $request, EntityManagerInterface $em): JsonResponse
    {
        $contextId = $request->query->getInt('context');
        $context = $contextId ? $em->getRepository(Context::class)->find($contextId) : null;
        if (!$context) {
            return $this->json(['error' => 'Context is required'], Response::HTTP_BAD_REQUEST);
        }

        $parentId = $request->query->getInt('parentId') ?: null;
        $parent = $parentId ? $em->getRepository(CollectionFolder::class)->find($parentId) : null;

        $data = $this->parseImportFile($request, 'auditix-command-folder-export');
        if ($data instanceof JsonResponse) return $data;

        $em->beginTransaction();
        try {
            $this->importCmdFolderTree($data['folder'], $parent, $context, $em);
            $em->flush();
            $em->commit();
            return $this->json(['success' => true], Response::HTTP_CREATED);
        } catch (\Throwable $e) {
            $em->rollback();
            return $this->json(['error' => 'Import failed: ' . $e->getMessage()], Response::HTTP_INTERNAL_SERVER_ERROR);
        }
    }

